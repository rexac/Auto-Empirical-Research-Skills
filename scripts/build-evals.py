#!/usr/bin/env python3
"""Validate and render flagship AERS evaluation prompts."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
DEFAULT_SOURCE = ROOT / "evals" / "flagship-evals.json"
DEFAULT_MARKDOWN = ROOT / "docs" / "EVALS.md"
REQUIRED_EVAL_FIELDS = {
    "id",
    "title",
    "category",
    "skill_path",
    "prompt",
    "expected_artifacts",
    "checks",
    "failure_modes",
    "tags",
}


def rel(path: Path) -> str:
    try:
        return path.relative_to(ROOT).as_posix()
    except ValueError:
        return path.as_posix()


def load_payload(path: Path) -> dict[str, Any]:
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except OSError as exc:
        raise ValueError(f"cannot read {rel(path)}: {exc}") from exc
    except json.JSONDecodeError as exc:
        raise ValueError(f"{rel(path)} is not valid JSON: {exc}") from exc
    if not isinstance(data, dict):
        raise ValueError(f"{rel(path)} must contain a JSON object")
    return data


def require_string(value: Any, field: str, eval_id: str) -> str:
    if not isinstance(value, str) or not value.strip():
        raise ValueError(f"{eval_id}: `{field}` must be a non-empty string")
    return value.strip()


def require_string_list(value: Any, field: str, eval_id: str) -> list[str]:
    if not isinstance(value, list) or not value:
        raise ValueError(f"{eval_id}: `{field}` must be a non-empty list")
    items: list[str] = []
    for index, item in enumerate(value):
        if not isinstance(item, str) or not item.strip():
            raise ValueError(f"{eval_id}: `{field}[{index}]` must be a non-empty string")
        items.append(item.strip())
    return items


def validate_payload(payload: dict[str, Any], source_path: Path) -> dict[str, Any]:
    if payload.get("schema_version") != "1.0":
        raise ValueError(f"{rel(source_path)} must use schema_version 1.0")

    evals = payload.get("evals")
    if not isinstance(evals, list) or not evals:
        raise ValueError(f"{rel(source_path)} must contain a non-empty `evals` list")

    seen_ids: set[str] = set()
    normalized: list[dict[str, Any]] = []
    for index, record in enumerate(evals):
        if not isinstance(record, dict):
            raise ValueError(f"evals[{index}] must be an object")

        missing = sorted(REQUIRED_EVAL_FIELDS.difference(record))
        eval_id = str(record.get("id", f"evals[{index}]"))
        if missing:
            raise ValueError(f"{eval_id}: missing required fields: {', '.join(missing)}")

        eval_id = require_string(record["id"], "id", eval_id)
        if eval_id in seen_ids:
            raise ValueError(f"duplicate eval id: {eval_id}")
        seen_ids.add(eval_id)

        skill_path = require_string(record["skill_path"], "skill_path", eval_id)
        if not (ROOT / skill_path).is_file():
            raise ValueError(f"{eval_id}: skill_path does not exist: {skill_path}")

        normalized.append(
            {
                "id": eval_id,
                "title": require_string(record["title"], "title", eval_id),
                "category": require_string(record["category"], "category", eval_id),
                "skill_path": skill_path,
                "prompt": require_string(record["prompt"], "prompt", eval_id),
                "expected_artifacts": require_string_list(
                    record["expected_artifacts"], "expected_artifacts", eval_id
                ),
                "checks": require_string_list(record["checks"], "checks", eval_id),
                "failure_modes": require_string_list(
                    record["failure_modes"], "failure_modes", eval_id
                ),
                "tags": require_string_list(record["tags"], "tags", eval_id),
            }
        )

    return {
        "schema_version": "1.0",
        "description": require_string(payload.get("description"), "description", "root"),
        "evals": sorted(normalized, key=lambda item: item["id"]),
    }


def markdown_escape(text: str) -> str:
    return text.replace("|", "\\|").replace("\n", " ").strip()


def render_markdown(payload: dict[str, Any], source_path: Path) -> str:
    evals: list[dict[str, Any]] = payload["evals"]
    categories = sorted({record["category"] for record in evals})
    lines = [
        "# Flagship Skill Evaluation Prompts",
        "",
        "This file is generated by `scripts/build-evals.py` from `evals/flagship-evals.json`. Do not edit it by hand.",
        "",
        payload["description"],
        "",
        "## Snapshot",
        "",
        f"- Eval prompts: {len(evals)}",
        f"- Categories: {', '.join(categories)}",
        f"- Source: [`{rel(source_path)}`](../{rel(source_path)})",
        "",
        "## How To Use",
        "",
        "- Use these prompts as regression tests after changing flagship skills or routing docs.",
        "- Review outputs against the checks and failure modes; this file defines expectations, not an automatic grade.",
        "- Add or update prompts whenever a new first-party flagship skill becomes a recommended entry point.",
        "",
        "## Eval Matrix",
        "",
        "| ID | Category | Skill | Tags |",
        "|---|---|---|---|",
    ]

    for record in evals:
        lines.append(
            "| `{id}` | {category} | [`SKILL.md`](../{skill_path}) | {tags} |".format(
                id=markdown_escape(record["id"]),
                category=markdown_escape(record["category"]),
                skill_path=record["skill_path"],
                tags=", ".join(f"`{markdown_escape(tag)}`" for tag in record["tags"]),
            )
        )

    lines.append("")
    for record in evals:
        lines.extend(
            [
                f"## {record['title']}",
                "",
                f"- ID: `{record['id']}`",
                f"- Category: `{record['category']}`",
                f"- Skill: [`{record['skill_path']}`](../{record['skill_path']})",
                "",
                "Prompt:",
                "",
                f"> {record['prompt']}",
                "",
                "Expected artifacts:",
            ]
        )
        lines.extend(f"- `{artifact}`" for artifact in record["expected_artifacts"])
        lines.extend(["", "Review checks:"])
        lines.extend(f"- {check}" for check in record["checks"])
        lines.extend(["", "Failure modes:"])
        lines.extend(f"- {failure}" for failure in record["failure_modes"])
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def write_output(markdown: str, markdown_path: Path) -> None:
    markdown_path.parent.mkdir(parents=True, exist_ok=True)
    markdown_path.write_text(markdown, encoding="utf-8")


def check_output(markdown: str, markdown_path: Path) -> int:
    if not markdown_path.exists() or markdown_path.read_text(encoding="utf-8") != markdown:
        print("Eval docs are stale. Regenerate with `make evals` or `make catalog`.", file=sys.stderr)
        print(f"stale: {rel(markdown_path)}", file=sys.stderr)
        return 1
    print("Eval docs are current.")
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--source", type=Path, default=DEFAULT_SOURCE)
    parser.add_argument("--markdown", type=Path, default=DEFAULT_MARKDOWN)
    parser.add_argument("--check", action="store_true", help="fail if generated docs are stale")
    args = parser.parse_args()

    try:
        source_path = args.source.resolve()
        markdown_path = args.markdown.resolve()
        payload = validate_payload(load_payload(source_path), source_path)
        markdown = render_markdown(payload, source_path)
    except ValueError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 1

    if args.check:
        return check_output(markdown, markdown_path)

    write_output(markdown, markdown_path)
    print(f"Wrote {rel(markdown_path)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
